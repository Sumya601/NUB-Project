<script>
    // Get the password input element
    var passwordInput = document.getElementById('password');

    // Add event listener for input changes
    passwordInput.addEventListener('input', function() {
        // Get the entered password
        var password = passwordInput.value;

        // Create a regular expression to check password strength
        var regex = /^(?=.*[a-z])(?=.*[A-Z])(?=.*\d).{8,}$/;

        // Check if the password meets the strength requirements
        if (!regex.test(password)) {
            // Show an error message
            passwordInput.setCustomValidity('Please enter a strong password. Use uppercase, lowercase, number');
            passwordInput.reportValidity();
        } else {
            // Clear the error message
            passwordInput.setCustomValidity('');
        }
    });
</script>
