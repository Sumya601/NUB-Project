            <h3><span lang="bn">ভাত শালিক (common myna)</span></h3>
            <p>
              <br><br>
              <span lang="bn">ভাত শালিক (বৈজ্ঞানিক নাম: Acridotheres tristis) Sturnidae (স্টার্নিডি) গোত্র বা পরিবারের অন্তর্গত Acridotheres (অ্যাক্রিডোথিরিস) গণের অন্তর্গত অত্যন্ত পরিচিত একটি পাখি। ভাতশালিকের বৈজ্ঞানিক নামের অর্থও অনুজ্জ্বল পঙ্গপালভূক (গ্রিক: akridos = পঙ্গপাল, theres = শিকারী; লাতিন: tristis = অনুজ্জ্বল বর্ণের)। পাখিটি বাংলাদেশ, ভারত ছাড়াও দক্ষিণ ও দক্ষিণ-পূর্ব এশিয়ার বিভিন্ন দেশে দেখা যায়। সারা পৃথিবীতে এক বিশাল এলাকা জুড়ে এদের আবাস, প্রায় ৭১ লক্ষ ৯০ হাজার বর্গ কিলোমিটার এলাকা জুড়ে এদের আবাস। বিগত কয়েক দশক ধরে এদের সংখ্যা ক্রমেই বাড়ছে। সেকারণে আই. ইউ. সি. এন. এই প্রজাতিটিকে ন্যূনতম বিপদগ্রস্ত বলে ঘোষণা করেছে। বাংলাদেশের বন্যপ্রাণী আইনে এ প্রজাতিটি সংরক্ষিত।

                ভাত শালিক পৃথিবীর বিভিন্ন দেশে ছড়িয়ে পড়েছে এবং ক্রমেই এ ছড়ানোর হার দ্রুতগতিতে বাড়ছে। আইইউসিএন কর্তৃক প্রজাতিটি অন্যতম অনুপ্রবেশকারী ক্ষতিকর প্রজাতি হিসেবে ঘোষিত হয়েছে। বিশেষ করে অস্ট্রেলিয়ায় ভাতশালিক সর্বোচ্চ ক্ষতিকর প্রজাতি হিসেবে চিহ্নিত। প্রজাতিটি সমগ্র পৃথিবীব্যাপী মানবস্বার্থ, বাস্তুতন্ত্র, কৃষি ও জীববৈচিত্র্যের প্রতি হুমকিস্বরূপ।
            <br><br>
            <strong>দৈহিক বিবরণ</strong><br>
            ভাত শালিকের দেহের বেশিরভাগ অংশ জুড়ে রয়েছে বাদামি রঙ। প্রাপ্তবয়স্ক পাখির মাথা ও ঘাড় কালচে। বুকের উপরের অংশ ও লেজ-উপরি ঢাকনিও কালো। দেহের বাকি অংশ কালচে বাদামি। কোন ঝুঁটি নেই। এর ডানার সাদা পট্টি ওড়ার সময় স্পষ্ট হয়। একই ভাবে স্পষ্ট হয় লেজের প্রান্তভাগ। অবসারণী-ঢাকনি সাদা। চোখের নিচে ও পেছনের পালকহীন চামড়া হলুদ। চোখ বাদামি বা লালচে বাদামি। ঠোঁট হলুদ। নিচের ঠোঁটের গোড়া সামান্য বাদামি-সবুজ। পা, পায়ের পাতা ও নখর হলুদ। স্ত্রী ও পুরুষ পাখির চেহারা একই রকম, কেবল আকারে সামান্য ভিন্নতা দেখা যায়। অপ্রাপ্তবয়স্ক পাখির মাথা সামান্য বাদামি-কালো এবং গলা ও বুক অপেক্ষাকৃত ফিকে বাদামি।

ভাত শালিক গ্লোজারের নীতি মেনে চলে, অর্থাৎ উত্তর-পশ্চিম ভারতের সদস্যরা দক্ষিণের সদস্যদের তুলনায় তুলনামূলক ফিকে রঙের।
<br><br><strong>খাদ্য ও খাদ্যাভ্যাস</strong><br>
অন্যসব শালিকের মত ভাত শালিকও সর্বভূক। শহর, গ্রাম, প্রান্তর, ডাস্টবিন সর্বত্রই এরা খাবার খুঁজে বেড়ায়। এদের খাদ্যতালিকায় রয়েছে পোকামাকড়, শুঁয়োপোকা, কেঁচো, ফল, শস্যদানা, বীজ, ছোট সরীসৃপ ও স্তন্যপায়ী এবং মানুষের ফেলে দেওয়া খাবার ও উচ্ছিষ্ট। সুযোগ পেলে এরা মরা ছোটখাটো প্রাণীও খায়। বড় বড় ফুলের মধু ও খেজুরের রস এদের খুবই পছন্দ। খাদ্যের সন্ধানে এদের গতিবিধি বিভিন্ন প্রজাতির ফুলের পরাগায়নে গুরুত্বপূর্ণ ভূমিকা পালন করে। ফলের মধ্যে নরম ফল, বিশেষ করে পাকা বট, আম, জাম, পেয়ারা, সবেদা, আতা ইত্যাদি খাওয়ায় পারদর্শী।[২] পোকামাকড়ের সন্ধানে ঘাসজমিতে এদের প্রায়ই লাফিয়ে লাফিয়ে চরে বেড়াতে দেখা যায়। মূলত এরা ঘাসফড়িং বা পঙ্গপাল খুঁজে খুঁজে শিকার করে খায়। সেজন্যই এর বৈজ্ঞানিক নাম হয়েছে Acridotheres বা পঙ্গপাল শিকারী। শুধু ভূমি থেকে নয়, অন্যান্য উৎস থেকেও বিচিত্র রকমের পতঙ্গ এরা আহার করে। পোকামাকড়ের সন্ধানে এরা তৃণভূমিতে গবাদিপশুর পেছনে পেছনে ঘুরে বেড়ায়। গবাদিপশুর চলাফেরার ফলে ঘাসের মধ্যে লুকিয়ে থাকা পোকামাকড় বাইরে বেরিয়ে এলে এরা সুযোগ বুঝে সেগুলোকে শিকার করে। এছাড়া দাবানল লাগলে তৃণভূমি থেকে পোকামাকড় বেরিয়ে আসে। তখনও ভাতশালিককে পতঙ্গ-শিকার করতে দেখা গেছে।[৯] এছাড়া সদ্য চাষ দেওয়া জমিতে একইভাবে বেরিয়ে আসা পোকামাকড় খায়। বন্দী অবস্থায় এরা ভাতও খায়
