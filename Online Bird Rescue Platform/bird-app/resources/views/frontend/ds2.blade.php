              <h3>Parrot Fever (Psittacosis)</h3><span lang="bn">
            <p>
            Parrot fever is a rare infection caused by Chlamydia psittaci, a specific type of bacteria. The infection is also known as parrot disease and psittacosis. 
            The United States has seen fewer than 10 human cases of parrot fever each year 
            since 2010. However, many cases may be undiagnosed or unreported because the symptoms are similar 
            to those of other illnesses.
            <br><br>
            As the name suggests, the disease is acquired from birds. Parrots are not the only possible culprits, however. Other wild and pet birds may also carry the infection and pass it to humans.
            <br><br>
            Parrot fever has been reported in countries including Argentina, Australia, and England. It may be found anywhere birds are kept as pets or in large confined populations (such as poultry farms). It’s more common in tropical environments.
            <br><br>
            <strong>Contracting parrot fever</strong><br>
            In most cases, humans catch parrot fever from birds, including:

              <br><br> &#9632; parrots
              <br> &#9632; chickens
              <br> &#9632; turkeys
              <br> &#9632; pigeons
              <br> &#9632; parakeets
              <br> &#9632; cockatiels
              <br> &#9632; ducks<br>
              <br>You can catch parrot fever by handling an infected bird or breathing in fine particles of
               its urine, feces, or other bodily excretions. You may also become infected if the bird
                bites you or “kisses” you by touching its beak to your mouth.<br>

            Catching the disease from an infected person is also possible, but very rare. This may occur when you inhale the fine droplets that are sprayed into the air when the sick person coughs.
            <br><br>
            <strong>Recognizing a bird with parrot fever</strong><br>
            Infected birds don’t necessarily show symptoms. They can also carry the bacteria for months before any outward signs appear. Just because a bird does not look or act sick does not mean that it’s not infected.

            Infected birds may shiver or have difficulty breathing. Other symptoms include:
              <br><br> &#9632; discharge from the eyes or nose
              <br> &#9632; diarrhea
              <br> &#9632; discolored droppings (urine or feces) in various shades of green
              <br> &#9632; weight loss
              <br> &#9632; lethargy and sleepiness<br><br>
            The sick bird may eat less or even stop eating completely.
            <br><br>
            <strong>Symptoms</strong><br>
            In people, this disease typically resembles the flu or pneumonia. Symptoms usually begin approximately 10 days after exposure, but they may take as few as four days or as many as 19 days to show up.

            Parrot fever has many of the symptoms that you might associate with the flu, including:

            <br><br> &#9632; fever and chills
            <br> &#9632; nausea and vomiting
            <br> &#9632; muscle and joint pain
            <br> &#9632; diarrhea
            <br> &#9632; weakness
            <br> &#9632; fatigue
            <br> &#9632; cough (typically dry)<br><br>
            Other possible symptoms, which may not seem flu-like, include chest pain, shortness of breath, and sensitivity to light.
            <br><br>
            <strong>Treatment</strong><br>
            Parrot fever is treated with antibiotics. Tetracycline and doxycycline are two antibiotics that are effective against this disease. However, your doctor may sometimes choose to treat you with other types or classes of antibiotics. Very young children may be treated with azithromycin.
            <br>After diagnosis, antibiotic treatment typically continues for 10 to 14 days after the fever resolves.
            <br>Most people who are treated for parrot fever make a full recovery. However, recovery may be slow in people who are older, very young, or who have other health issues. Still, parrot fever rarely causes death in humans who have received proper treatment.
            <br><br>
            <strong>Prevention</strong><br>
            If you have pet birds, you can take steps to reduce your chances of getting parrot fever. These include cleaning your birdcages every day and taking good care of your birds to help prevent them from getting sick. Feed your birds properly and give them enough space so they’re not crowded together in the cage. If you have more than one cage, make sure the cages are far apart so that feces and other matter can’t be transferred between them.
            <br>If you acquire a new bird, have it looked at by a veterinarian. It’s good to then isolate the bird and monitor it for sickness for at least 30 days before you allow it to contact other birds.
            <br>If you see a sick or dead bird (whether it’s wild or a pet), you should not touch it. Contact your city’s animal control service to remove a dead wild bird. If it’s a pet, you should use caution when touching or moving it. Use gloves and a mask to avoid breathing in any bacteria, feather dust, or other debris. You should also disinfect the cage and all equipment the bird has used to prevent infection or reinfection.
